package org.simulationsystems.csf.common.csfmodel;

public class CsfSimulationMessagingRuntimeException extends RuntimeException {

	public CsfSimulationMessagingRuntimeException() {
		super();
	}

	public CsfSimulationMessagingRuntimeException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public CsfSimulationMessagingRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CsfSimulationMessagingRuntimeException(String arg0) {
		super(arg0);
	}

	public CsfSimulationMessagingRuntimeException(Throwable arg0) {
		super(arg0);
	}

}
